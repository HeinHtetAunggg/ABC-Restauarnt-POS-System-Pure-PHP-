<script>
    const imageContainerEl=document.querySelector(".branch-container");
const prevbtn=document.getElementById("prev");
const nextbtn=document.getElementById("next");
const containerEl=document.querySelector(".branchtext-container");

const branches=["Yangon", "Norway", "Brazil", "Sweden", "Taiwan","Canada", "Mexico", "Russia "]


let branchesIndex=0;
let characterIndex=0;
let x=0;
let timer;

updateText();

function updateText(){
    characterIndex++;
    containerEl.innerHTML=` <h2>${branches[branchesIndex].slice(0,characterIndex)} Branch</h2>`

    if(characterIndex ===branches[branchesIndex].length){
        characterIndex=0;
        branchesIndex++;
    }
    if(branchesIndex === branches.length){
        branchesIndex=0;
    }
    setTimeout(updateText,490);
    
}



prevbtn.addEventListener("click",()=>{
    x = x+45;
    clearTimeout(timer);
    updateGallery();
})

nextbtn.addEventListener("click",()=>{
    x= x-45;
    clearTimeout(timer);
    updateGallery();
})


function updateGallery(){
    imageContainerEl.style.transform=`perspective(1000px) rotateY(${x}deg)`;
   timer= setTimeout(()=>{
        x=x-45;
      updateGallery()
    },3000)

}

updateGallery();
</script>


